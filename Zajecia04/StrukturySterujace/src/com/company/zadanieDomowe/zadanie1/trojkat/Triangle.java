package com.company.zadanieDomowe.zadanie1.trojkat;

public class Triangle {
    private int bok1;
    private int bok2;
    private int bok3;

    public Triangle(int bok1, int bok2, int bok3) {
        this.bok1 = bok1;
        this.bok2 = bok2;
        this.bok3 = bok3;
    }

    public int getBok1() {
        return bok1;
    }

    public void setBok1(int bok1) {
        this.bok1 = bok1;
    }

    public int getBok2() {
        return bok2;
    }

    public void setBok2(int bok2) {
        this.bok2 = bok2;
    }

    public int getBok3() {
        return bok3;
    }

    public void setBok3(int bok3) {
        this.bok3 = bok3;
    }

}
