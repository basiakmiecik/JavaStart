public class kalkulator {
    public static void main(String[] args){
        double x=5.0;
        int y=3;
        double add=x+y;
        double sub=x-y;
        double diff=x/y;
        double multi=x*y;
        System.out.println(x+"+"+y+"="+add);
        System.out.println(x+"-"+y+"="+sub);
        System.out.println(x+"/"+y+"="+diff);
        System.out.println(x+"*"+y+"="+multi);
    }

}
